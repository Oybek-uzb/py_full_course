name = "Shohjahon"

print(len(name)) # length
print(name.find("j")) # j turgan joyning indeksi, agar belgi topilmasa -1 qaytariladi
print(name.capitalize()) # faqat birinchi belgini yuqori registerga qolganlarini esa quyi registerga o'tkazadi
print(name.upper()) # tushunarli
print(name.lower()) # tushunarli
print(name.isdigit()) # name ni sonlikka tekshiradi
print(name.isalpha()) # name ni harflikka tekshiradi, ' ' harfga kirmaydi
print(name.count('o')) # name dagi "o" larni sanaydi
print(name.replace("o", "a")) # name dagi "o" larni "a" larga almashtiradi
